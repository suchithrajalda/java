class Vowels
{
	public static void main(String args[])
	{
		char vowel='s';
		if(vowel>=65 && vowel<=90)
		{
			if(vowel=='A' || vowel=='E' || vowel=='I' || vowel=='O' || vowel=='U')
		        {
			System.out.println("vowel");
			}
			else
			{
			System.out.println("consonant");
		}}
		else if(vowel>=97 && vowel<=122)
		{
			if(vowel=='a' || vowel=='e' || vowel=='i' || vowel=='o' || vowel=='u')
		        {
			System.out.println("vowel");
			}
			else
			{
			System.out.println("consonant");
		}
		
		}
	
	}
}